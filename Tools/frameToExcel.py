# Python script that uses 'input' command to receive a string from the command line
# The script will remove ".word" from the string and format the remaining numbers for Excel.

def format_for_excel(input_string):
    # Splitting the string into lines
    lines = input_string.split('\n')
    
    # Removing ".word" and leading/trailing spaces from each line
    formatted_lines = [line.replace('.word', '').strip() for line in lines]

    # Joining the lines back into a single string
    return '\n'.join(formatted_lines)

